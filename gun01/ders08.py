# aritmetiksel operatörler
print(35 * 9) # * çarpma için kullanılan operatör
print(35 / 9) # / bölme için kullanılan operatör
print(35 % 9) # % mod alma için kullanılan operatör
print(35 + 9)# + toplama için kullanılır
print(35 - 9)# - çıkartma için kullanılır
print(35 ** 2)# ** üs alma işlemi için kullanılır
print(35 // 9)# // tam bölme işlemi için kullanılır

